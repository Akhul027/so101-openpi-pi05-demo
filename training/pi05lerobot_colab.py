def looks_like_model_dir(p: Path) -> bool:
    return p.is_dir() and any((p / f).exists() for f in ["config.json", "model.safetensors", "train_config.json"])

# Preference order:
# 1. checkpoints/last/pretrained_model  (what from_pretrained expects)
# 2. newest checkpoints/<step>/pretrained_model
# 3. any pretrained_model dir
CHECKPOINT_DIR = None

last_pm = out / "checkpoints" / "last" / "pretrained_model"
if looks_like_model_dir(last_pm):
    CHECKPOINT_DIR = last_pm
else:
    pm_dirs = sorted(p for p in out.glob("checkpoints/*/pretrained_model") if looks_like_model_dir(p))
    if pm_dirs:
        CHECKPOINT_DIR = pm_dirs[-1]
    else:
        pm_dirs = sorted(p for p in out.rglob("pretrained_model") if looks_like_model_dir(p))
        if pm_dirs:
            CHECKPOINT_DIR = pm_dirs[-1]

if CHECKPOINT_DIR is None:
    print("\nNo trained checkpoint found under", out)
    print("The inference cell will fall back to the base model.")
    CHECKPOINT_DIR = ""
else:
    CHECKPOINT_DIR = str(CHECKPOINT_DIR)
