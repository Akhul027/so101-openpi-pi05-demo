# This cell actually starts training WITH live progress visualization.
# For the first run, keep TRAIN_MODE='debug_full' so it only runs 10 steps.
#
# It streams lerobot-train's console output, parses the metric lines
# (step / loss / grdn / lr), and redraws a matplotlib chart every few
# log lines. Full raw logs are still printed below the chart.

import re
import subprocess
import collections
import matplotlib.pyplot as plt
from IPython.display import display, clear_output

# lerobot logs steps via format_big_number: "250", "1K", "1.5K", "2M", ...
_SUFFIX = {"": 1, "K": 1e3, "M": 1e6, "B": 1e9, "T": 1e12, "Q": 1e15}

def parse_big_number(s):
    m = re.fullmatch(r"([\d.]+)([KMBTQ]?)", s.strip())
    if not m:
        return None
    return float(m.group(1)) * _SUFFIX[m.group(2)]

METRIC_RE = re.compile(
    r"step:(?P<step>[\d.]+[KMBTQ]?).*?"
    r"loss:(?P<loss>[\d.eE+-]+)"
    r"(?:.*?grdn:(?P<grdn>[\d.eE+-]+))?"
    r"(?:.*?lr:(?P<lr>[\d.eE+-]+))?"
)

history = {"step": [], "loss": [], "grdn": [], "lr": []}
log_tail = collections.deque(maxlen=15)  # last lines shown under the chart

def redraw():
    clear_output(wait=True)
    fig, axes = plt.subplots(1, 3, figsize=(15, 3.5))

    axes[0].plot(history["step"], history["loss"], marker=".", lw=1)
    axes[0].set_title("loss"); axes[0].set_xlabel("step"); axes[0].grid(alpha=0.3)
    if history["loss"] and min(history["loss"]) > 0:
        axes[0].set_yscale("log")

    g = [(s, v) for s, v in zip(history["step"], history["grdn"]) if v is not None]
    if g:
        axes[1].plot([x for x, _ in g], [y for _, y in g], marker=".", lw=1, color="tab:orange")
    axes[1].set_title("grad norm"); axes[1].set_xlabel("step"); axes[1].grid(alpha=0.3)

    l = [(s, v) for s, v in zip(history["step"], history["lr"]) if v is not None]
    if l:
        axes[2].plot([x for x, _ in l], [y for _, y in l], marker=".", lw=1, color="tab:green")
    axes[2].set_title("learning rate"); axes[2].set_xlabel("step"); axes[2].grid(alpha=0.3)

    if history["step"]:
        fig.suptitle(
            f"{job_name}   |   step {history['step'][-1]:.0f} / {steps}   |   "
            f"last loss {history['loss'][-1]:.4f}",
            y=1.05,
        )
    plt.tight_layout(); plt.show()

    print("---- recent log lines ----")
    for line in log_tail:
        print(line)

print("Starting training...")
print("Output dir:", output_dir)

proc = subprocess.Popen(
    cmd, stdout=subprocess.PIPE, stderr=subprocess.STDOUT,
    text=True, bufsize=1,
)

lines_since_draw = 0
try:
    for raw_line in proc.stdout:
        line = raw_line.rstrip()
        if not line:
            continue
        log_tail.append(line)

        m = METRIC_RE.search(line)
        if m:
            step_val = parse_big_number(m.group("step"))
            if step_val is not None:
                history["step"].append(step_val)
                history["loss"].append(float(m.group("loss")))
                history["grdn"].append(float(m.group("grdn")) if m.group("grdn") else None)
                history["lr"].append(float(m.group("lr")) if m.group("lr") else None)
                redraw()
                lines_since_draw = 0
                continue

        # Refresh the log tail periodically even when no metric line appears
        # (e.g. during the long model download / compile phase).
        lines_since_draw += 1
        if lines_since_draw >= 40:
            redraw()
            lines_since_draw = 0
finally:
    proc.stdout.close()
    returncode = proc.wait()

redraw()
if returncode != 0:
    raise RuntimeError(f"Training command failed with return code {returncode}")

print("\nTraining finished.")

